// Declarar cada nota como constante
const nota1 = 4.5;
const nota2 = 3.8;
const nota3 = 2.9;
const nota4 = 4.0;

// Calcular la suma de las 4 notas
let suma = nota1 + nota2 + nota3 + nota4;

// Calcular el promedio
let promedio = suma / 4;

// Calcular el resultado de la expresión
let resultadopromedio = (nota1 + nota2) * (nota3 + nota4) / 2;

// Mostrar los resultados
console.log("La suma de las 4 notas es: " + suma);
console.log("El promedio de las 4 notas es: " + promedio);
console.log("El resultado de la operación es: " + resultadopromedio);

