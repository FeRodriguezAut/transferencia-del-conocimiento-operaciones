let dias = horas / 24;

//impresion de resultados
console.log(`Los 7200 segundos de actividad física equivalen a:`);
console.log(`- ${minutos} minutos.`);
console.log(`- ${horas} horas.`);
console.log(`- ${dias} días.`);


